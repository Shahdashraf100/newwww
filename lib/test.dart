void main()
{
  int sum(int x,int y)
  {
    return x+y;
  }

  int sub(int x,int y)
  {
    return x-y;
  }

  int mult(int x,int y)
  {
    return x * y;
  }
  double div(int x,int y)
  {
    return x / y;
  }

  int x=2;
  int y=3;
  print(sum(x, y));
  print(sub(x, y));
  print(mult(x, y));
  print(div(x, y));



}